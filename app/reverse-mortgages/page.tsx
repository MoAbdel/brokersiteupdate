          <p className="text-yellow-800 mb-0">
            When a reverse mortgage borrower passes away, heirs have 30 days to notify the lender and 6 months (with possible extensions) to repay the loan or sell the home. The <strong>non-recourse feature</strong> means heirs never owe more than 95% of the home's appraised value, even if the loan balance exceeds the home's worth. Heirs can walk away with no financial obligation if the home is underwater.
          </p>
